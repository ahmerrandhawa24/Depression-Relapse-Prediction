def clean_previous_episodes(val):
    if pd.isna(val):
        return pd.NA
    val = str(val)

    # Handle NR or missing
    if 'NR' in val:
        return pd.NA

    # Handle median
    median_match = re.search(r'median\s*(\d+\.?\d*)', val, re.IGNORECASE)
    if median_match:
        return float(median_match.group(1))

    # Handle simple numbers
    numbers = re.findall(r'\d+\.?\d*', val)
    if numbers:
        numbers = [float(n) for n in numbers]
        # Take average if multiple numbers
        return sum(numbers)/len(numbers)

    return pd.NA

data['Previous_Episodes'] = data['Previous_Episodes'].apply(clean_previous_episodes)



