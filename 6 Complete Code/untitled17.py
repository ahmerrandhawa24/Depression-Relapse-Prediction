import re

def extract_first_number(val):
    if pd.isna(val):
        return pd.NA
    val = str(val)
    # Handle ranges like 24–272 → take the largest number
    if '–' in val:
        numbers = re.findall(r'\d+\.?\d*', val)
        if numbers:
            return float(numbers[-1])
    # Handle comma-separated → take max
    elif ',' in val:
        numbers = re.findall(r'\d+\.?\d*', val)
        if numbers:
            return float(numbers[-1])
    # Regular case → take first number
    else:
        match = re.search(r'\d+\.?\d*', val)
        if match:
            return float(match.group())
    return pd.NA

