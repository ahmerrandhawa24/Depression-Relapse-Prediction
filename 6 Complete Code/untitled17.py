import numpy as np

# Calculate relapse risk in each group
data['Risk_Exp'] = data['Exp_Relapse'] / data['Exp_Total']
data['Risk_Ctrl'] = data['Ctrl_Relapse'] / data['Ctrl_Total']

# Calculate Risk Ratio
data['RR'] = data['Risk_Exp'] / data['Risk_Ctrl']

data[['Study','Risk_Exp','Risk_Ctrl','RR']]

data['logRR'] = np.log(data['RR'])

data['SE'] = np.sqrt(
    (1/data['Exp_Relapse']) -
    (1/data['Exp_Total']) +
    (1/data['Ctrl_Relapse']) -
    (1/data['Ctrl_Total'])
)

data['CI_low'] = data['logRR'] - 1.96 * data['SE']
data['CI_high'] = data['logRR'] + 1.96 * data['SE']

data['RR_low'] = np.exp(data['CI_low'])
data['RR_high'] = np.exp(data['CI_high'])

